package Conditional_statements;

public class ElseIfInsideIf {
    
    public static void main(String[] args){
        int n1 = 20;
        int n2 = 30;

        if (n1 == n2){
            System.out.println(n1 + " and " + n2 + " both are equal.");
        } else {
            System.out.println(n1 + " and " + n2 + " are not equal.");
        }
    }
}
